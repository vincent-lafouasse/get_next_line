#include "get_next_line.h"
#include <fcntl.h>
#include <stdio.h>

int	main(int argc, char** argv)
{
	int		fd;
	char	*line;

	if (argc != 2)
	{
		printf("Usage: %s text_file\n", argv[0]);
		return 1;
	}
	fd = open(argv[1], O_RDONLY);
	line = get_next_line(fd);
	while (line)
	{
		printf("%s", line);
		line = get_next_line(fd);
	}
}

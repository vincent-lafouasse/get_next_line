#include "get_next_line.h"
#include <fcntl.h>
#include <stdio.h>

#define PATH "./aux/short.txt"

int	main(void)
{
	int		fd;
	char	*line;

	fd = open(PATH, O_RDONLY);
	line = get_next_line(fd);
	while (line)
	{
		printf("%s", line);
		line = get_next_line(fd);
	}
}
